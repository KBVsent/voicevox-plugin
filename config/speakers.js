// 获取所有可用的 speaker 别名列表
export function getSpeakerList() {
  const list = []
  for (const [name, id] of Object.entries(SPEAKER_MAP)) {
    list.push({ name, id })
  }
  return list.sort((a, b) => a.id - b.id)
}

// 根据名称或ID查找 speaker
export function findSpeaker(input) {
  // 如果是纯数字，直接返回
  const numId = parseInt(input)
  if (!isNaN(numId) && numId >= 0) {
    return numId
  }

  // 查找别名映射
  const lowercaseInput = input.toLowerCase()
  for (const [name, id] of Object.entries(SPEAKER_MAP)) {
    if (name.toLowerCase() === lowercaseInput) {
      return id
    }
  }

  return null
}

// 根据ID查找对应的主要名称
export function getSpeakerName(id) {
  for (const [name, speakerId] of Object.entries(SPEAKER_MAP)) {
    if (speakerId === id) {
      return name
    }
  }
  return `ID: ${id}`
}
